#include <iostream>  
// Підключаємо стандартну бібліотеку вводу/виводу для роботи з cout, cin тощо

using namespace std;  
// Дозволяє використовувати об’єкти std (наприклад, cout, endl) без префікса std::

/* 
 * Оголошуємо перше перевантаження функції increment
 * Ця версія приймає змінну типу int ЗА ПОСИЛАННЯМ (int &var), 
 * що дозволяє змінювати її значення в головній програмі.
 */
void increment(int &var) {
    var = var + 1;  // Збільшуємо значення змінної var на 1
}

/*
 * Оголошуємо друге перевантаження функції increment
 * Приймає два аргументи: 
 *  - var — змінна типу int за посиланням, яку потрібно змінити,
 *  - expr — значення, на яке потрібно збільшити var.
 */
void increment(int &var, int expr) {
    var = var + expr;  // Збільшуємо значення змінної var на значення expr
}
// Головна функція програми — точка входу
int main(void) {
    int var = 0;  // Оголошуємо змінну var і присвоюємо їй початкове значення 0

    // Цикл for повторюється 10 разів: від i = 0 до i = 9 включно
    for (int i = 0; i < 10; i++) {
        // Перевіряємо: якщо i — НЕпарне (тобто має залишок при діленні на 2)
        if (i % 2) 
            increment(var);       // Викликаємо першу версію функції: збільшуємо var на 1
        else                     
            increment(var, i);    // Якщо i — парне, викликаємо другу версію функції: збільшуємо var на значення i
                // Пояснення: по кроках додавання до var буде таким:
        // i = 0 (парне) => var += 0  (var = 0)
        // i = 1 (непарне) => var += 1 (var = 1)
        // i = 2 (парне) => var += 2 (var = 3)
        // i = 3 (непарне) => var += 1 (var = 4)
        // i = 4 (парне) => var += 4 (var = 8)
        // i = 5 (непарне) => var += 1 (var = 9)
        // i = 6 (парне) => var += 6 (var = 15)
        // i = 7 (непарне) => var += 1 (var = 16)
        // i = 8 (парне) => var += 8 (var = 24)
        // i = 9 (непарне) => var += 1 (var = 25)
    }

    cout << var << endl;  // Виводимо кінцеве значення var у консоль і переходимо на новий рядок
    return 0;             // Повертаємо 0 — означає успішне завершення програми
}
